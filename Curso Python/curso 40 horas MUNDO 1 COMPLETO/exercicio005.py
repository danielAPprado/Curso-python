n = int(input('Escreva um número ao qual deseje o antecessor e sucessor '))
print('O sucessor do seu número é {}, e o antecessor do seu número é {}'.format(n+1, n-1))

nn = int(input('Escreva um outro número '))
print('O dobro do seu número é {}, o seu triplo é {}, e sua raiz quadrada é {:.2f}'.format(nn*2, nn*3, nn**(1/2)))
